public class Loops {
  public static void main(String[] args) {
    // 循环
    // while (condition) statement
    int count = 0;
    while (count < 10) {
      System.out.println("count: " + count);
      count++;
    }
    // do statement while ();
    do {
      System.out.println("Count: " + count);
      count++;
    } while (count < 20);
  }
}
